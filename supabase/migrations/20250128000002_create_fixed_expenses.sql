-- Create fixed_expenses table for user-configurable monthly fixed expenses
CREATE TABLE IF NOT EXISTS public.fixed_expenses (
  id bigint PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  user_id uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  name text NOT NULL,
  amount numeric NOT NULL,
  note text DEFAULT '',
  icon text DEFAULT '',
  currency text DEFAULT 'CAD',
  sort_order int DEFAULT 0,
  is_active boolean DEFAULT true,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- Enable RLS
ALTER TABLE public.fixed_expenses ENABLE ROW LEVEL SECURITY;

-- RLS Policies: Users can only access their own fixed expenses
CREATE POLICY "Users can view their own fixed expenses"
  ON public.fixed_expenses
  FOR SELECT
  USING (auth.uid() = user_id);

CREATE POLICY "Users can insert their own fixed expenses"
  ON public.fixed_expenses
  FOR INSERT
  WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Users can update their own fixed expenses"
  ON public.fixed_expenses
  FOR UPDATE
  USING (auth.uid() = user_id)
  WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Users can delete their own fixed expenses"
  ON public.fixed_expenses
  FOR DELETE
  USING (auth.uid() = user_id);

-- Index for performance
CREATE INDEX IF NOT EXISTS idx_fixed_expenses_user_id ON public.fixed_expenses(user_id);
CREATE INDEX IF NOT EXISTS idx_fixed_expenses_active ON public.fixed_expenses(user_id, is_active);

-- Unique index on (user_id, name) for upsert operations (template import)
CREATE UNIQUE INDEX IF NOT EXISTS idx_fixed_expenses_user_name ON public.fixed_expenses(user_id, name);

-- Updated_at trigger
CREATE OR REPLACE FUNCTION public.update_fixed_expenses_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trigger_fixed_expenses_updated_at
  BEFORE UPDATE ON public.fixed_expenses
  FOR EACH ROW
  EXECUTE FUNCTION public.update_fixed_expenses_updated_at();

